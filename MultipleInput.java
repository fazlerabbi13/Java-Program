
import java.util.Scanner;

public class MultipleInput {
    public static void main(String[] args) {
        Scanner dd = new Scanner(System.in);

        int a = dd.nextInt();
        int b = dd.nextInt();
        int c = dd.nextInt();

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
